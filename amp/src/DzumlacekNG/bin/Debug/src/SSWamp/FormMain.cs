		private void MainFormFormClosing(object sender, FormClosingEventArgs e)
		{
			// If a user is closing the form, ensure the user has shutdown the apps
        	if (e.CloseReason== CloseReason.UserClosing && listMP != null)
        	{
        		foreach (MonitoredProcess mp in listMP)
        		{
        			if (mp.isAlive()) e.Cancel = true;
        		}
        	}
        	// Else if the computer is closing the form, force kill all the apps
        	else if (e.CloseReason==CloseReason.TaskManagerClosing
        	        || e.CloseReason==CloseReason.WindowsShutDown
        	        || e.CloseReason==CloseReason.ApplicationExitCall
        			|| e.CloseReason== CloseReason.None)
        	{
        		killAll();
        	}
        	
        	if (e.Cancel==true)
        	{
        		MessageBox.Show("Please stop all servers before trying to exit.","Server(s) Running");
        	}
		}
		
		private void forceKill()
		{
			// Tell all the applications not to restart
			killAll();
			
			// Kill the applications without respawning
			Process.Start("taskkill", "/F /IM nginx.exe");
			Process.Start("taskkill", "/F /IM httpd.exe");
			Process.Start("taskkill", "/F /IM php-cgi.exe");
			Process.Start("taskkill", "/F /IM mysqld.exe");
			Process.Start("taskkill", "/F /IM mongod.exe");
			Process.Start("taskkill", "/F /IM memcached.exe");
		}
		
		private void killAll()
		{
			for (int i = listMP.Count-1; i >= 0; i--)
			{
				if (listMP[i].isAlive()) listMP[i].kill();
				listMP.RemoveAt(i);
			}
		}
		
